                    <div class="col-md-6">
                        <p class="text-uppercase"><strong>Objetivo</strong></p>
                        <p>Establecer una guía de conducta en los negocios para asegurar el correcto comportamiento de los colaboradores de Grupo DMI y de sus diferentes unidades de negocio, en las relaciones comerciales y laborales en todas sus empresas y centros de trabajo.</p>
                    </div>
                    <div class="col-md-6">
                        <p class="text-uppercase"><strong>Alcance</strong></p>
                        <p>Este código de ética es de aplicación para todo el personal que labora Grupo DMI, de cualquier nivel independientemente de su cargo o puesto, Directivos, Gerentes, Administrativos, Operativos o si son eventuales o permanentes, así como a todas las empresas, filiales, subsidiarias, centros de trabajo, y empresas en las que tenemos vínculos accionarios.</p>
                    </div>
                </div>
                <div>
                <p class="text-uppercase pt-3"><strong>DEFINICIONES</strong></p>
                <p><strong>Activos: </strong> Son todos aquellos bienes y derechos de propiedad de la empresa o controlados por
                ésta con el que la empresa puede realizar su actividad.</p>
                <p><strong>Conflicto de interés: </strong> La posible afectación del desempeño imparcial y objetivo de las funciones
                debido a intereses personales, familiares o de negocios.</p>
                <p>Surge cuando los intereses personales, familiares, financieros, privados o de cualquier otra
                    índole, afectan o pudieran afectar el desempeño y cumplimiento ético de los directivos y
                    colaboradores, o ser beneficiados directa o indirectamente por dichos intereses.
                </p>
                <p><strong>Corrupción: </strong> Consiste en el abuso de los recursos o los medios que tienen por su trabajo para
                beneficio propio.</p>
                <p><strong>Familiares: </strong> Cónyuge, parientes consanguíneos, parientes por afinidad y parientes civiles, hasta
                el segundo grado.</p>
                <p><strong>Ética profesional: </strong> conjunto de normas o principios por los cuales debe regirse una persona en
                el ámbito laboral.</p>
                <p><strong>Propiedad Industrial: </strong>  Son aquellos derechos de propiedad intelectual, entre los que figuran las
                patentes, los modelos de utilidad, las marcas, los secretos industriales, las políticas y los
                procedimientos internos de la empresa.</p>
                <p><strong>Riesgo: </strong>  Grado de probabilidad y exposición ante una amenaza que al materializarse causaría
                un impacto adverso.</p>

                <p class="text-uppercase pt-3"><u><strong>1. POLÍTICAS</strong></u></p>
                <p>La buena reputación de Grupo DMI, es el fruto del trabajo honesto y del esfuerzo realizado por todos
                    los que colaboramos en él.</p>
                <p>La integridad y la ética de todos los colaboradores deben de ser un factor de diferenciación y
                    reconocimiento ante nuestros clientes, proveedores, y sociedad en general.</p>
                <p>El presente código de ética es una guía de conducta de Grupo DMI, que fue creada para asegurar el
                    entendimiento y dominio de las conductas esperadas, las cuales seguirán manteniendo la marca y
                    nombre de Grupo DMI por encima de nuestros competidores.</p>
                <p>El presente código incluirá los lineamientos básicos de acuerdo con los cuales todos los
                    colaboradores de las diferentes empresas de Grupo DMI deben de actuar y tomar decisiones.</p>

                <p class="text-uppercase pt-3"><u><strong>2. Valores</strong></u></p>
                <p>En Grupo DMI buscamos fomentar las siguientes virtudes:</p>
                <ul>
                    <li>Honestidad.</li>
                    <li>Responsabilidad.</li>
                    <li>Justicia.</li>
                    <li>Caridad.</li>
                    <li>Profesionalidad.</li>
                </ul>
                <p class="text-uppercase pt-3"><u><strong>3. Conflicto de Interés</strong></u></p>
                <p>Cada colaborador de Grupo DMI es responsable de llevar a cabo los negocios de la empresa y las
                    relaciones personales dentro de la misma, con integridad y con estricto apego a la legalidad.
                    Todo colaborador deberá evitar situaciones en las que pudiera existir o entenderse un conflicto
                    entre sus intereses personales y los de Grupo DMI.</p>
                <p>Existe conflicto de intereses cuando intervienen o parecen intervenir intereses personales o de
                    terceros con los intereses de Grupo DMI.</p>
                <p>Al tratar con clientes, proveedores, prestadores de servicios, contratistas y competidores, sean reales
                    o potenciales, el colaborador deberá actuar guiado por el afán de lograr el mejor interés para Grupo
                    DMI, sin tratar de obtener beneficios o ventajas personales.</p>
                <p>Si alguna situación pudiera suponer un conflicto de intereses, el colaborador deberá solicitar apoyo
                    de su Coordinador de Recursos Humanos, lo más pronto posible mediante un escrito, explicando en
                    que consiste el conflicto de intereses, para su revisión.</p>
                <p>A continuación, se citan algunos ejemplos que son considerados como conflictos de interés, pero que
                    de ninguna manera constituyen el total de las situaciones en que se puede dar un conflicto de
                    intereses.</p>
                <p><strong>Hay conflicto de intereses cuando el colaborador de Grupo DMI y/o miembros de su familia:</strong></p>
                <ul>
                    <li>Tienen intereses financieros en alguna empresa ajena a Grupo DMI, que haga o trate de hacer
                        negocios con empresas de Grupo (comprar, vender o prestar servicios), o que sea competidora
                        de la misma.</li>
                    <li>Grupo DMI no puede contratar a un proveedor para el cual sus servicios o productos que nos
                        ofrezca representen más del 50% de sus ventas.</li>
                    <li>Laboran o participan en cualquier negocio o actividad que de manera directa o indirecta
                        compita, interfiera o trate de hacer negocio con Grupo DMI.</li>
                    <li>Son al mismo tiempo proveedor de algún servicio y/o producto a Grupo DMI</li>
                    <li>Actúan como agente o intermediario en beneficio de terceros, en transacciones que involucren
                        a Grupo DMI o a sus intereses.</li>
                </ul>
                <p><strong>También hay conflicto de intereses cuando el colaborador de Grupo DMI:</strong></p>
                <ul>
                    <li>Realiza trabajos para una empresa ajena que tenga o trate de hacer negocios con Grupo DMI,
                        o que sea competencia de la misma. Esto incluye, por ejemplo, ocupar cargos de director,
                        empleado, socio o consultor. </li>
                    <li>Percibe ingresos o beneficios de proveedores, competidores o clientes. </li>
                    <li>Incurre en deudas considerables con terceros cuyos negocios están relacionados de alguna
                        forma con Grupo DMI (clientes, proveedores, acreedores etc.) y puedan verse afectados por
                        las acciones realizadas por el colaborador, poniendo el riesgo el buen nombre del Grupo.</li>
                    <li>Toma provecho de su puesto o posición en la estructura de Grupo DMI para obtener beneficios
                        personales, de su familia o de terceras personas.</li>
                    <li>Toma provecho de su puesto o posición en la estructura de Grupo DMI para establecer o
                        participar en cualquier negocio relacionado con la actividad que desempeña dentro del Grupo.</li>
                    <li>Proporciona información confidencial a un tercero fuera de Grupo DMI independientemente
                        del uso que se le dé.</li>
                </ul>
                <p><strong><u>Reglas del Grupo DMI para evitar conflictos de interés</u></strong></p>
                <p>No está permitido a los colaboradores:</p>
                <ul>
                    <li>Contratar familiares en puestos de colaboradores. (Las excepción serán autorizadas por
                        Consejo DMI</li>
                    <li>Que un subordinado tenga como jefe directo a un familiar.</li>
                    <li>Que se tengan intereses económicos en proveer bienes y/o servicios en cualquiera de las otras
                        empresas parte del Grupo.</li>
                    <li>Laborar en alguna otra empresa que vaya en contra de los intereses o distraiga sus actividades
                        dentro de Grupo DMI. El colaborador que tenga otra forma de ingreso, deberá declararlo por
                        escrito a su jefe inmediato y al departamento de recursos humanos.</li>
                    <li>Recibir dinero en efectivo o depósitos en cuentas personales por transacciones o
                        negociaciones con un cliente o proveedor, en el que se involucre o relacione a Grupo DMI.</li>
                    <li>Ofrecer, prometer o vender productos en horarios de trabajo.</li>
                    <li>Utilizar indebidamente los medios electrónicos de la empresa para bajar y/o distribuir música,
                        pornografía, chistes, ofrecer productos o servicios personales o cualquier otro asunto no
                        relacionado con el negocio de las empresas de Grupo DMI.</li>
                    <li>Falsear u omitir información que perjudique los intereses de Grupo DMI.</li>
                    <li>Mantener contacto con personal que labore con empresas competidoras, para intercambiar
                        información confidencial.</li>
                </ul>
                <p><strong>Está prohibido que el colaborador de Grupo DMI y/o sus familiares:</strong></p>
                <ul>
                    <li>Tener negocios dedicados a la comercialización, distribución o transformación de los
                        productos o servicios del Grupo DMI.</li>
                    <li>Tener intereses económicos en empresas que proveen bienes o servicios en cualquiera de las
                        empresas del Grupo, sin la autorización apropiada.</li>
                    <li>Es responsabilidad del colaborador de Grupo DMI, de hacer del conocimiento al Comité de
                        Cumplimiento, de cualquier situación que vaya en contra de las reglas aquí definidas, ya sea
                        por incumplimiento de él mismo o de alguno de sus compañeros de trabajo. En el caso de ser
                        necesario, puede presentarse una denuncia anónima a través del Sistema de Denuncia.</li>
                </ul>
                <p class="text-uppercase pt-3"><u><strong>4. Protección y uso de la propiedad.</strong></u></p>
                <p>Todo colaborador de Grupo DMI es responsable de la protección de la propiedad intelectual
                    del Grupo DMI y del buen uso de sus bienes, así como de la información exclusiva que ésta
                    posee.</p>
                <p>
                    Nadie está autorizado para sacar provecho personal de la información privada que haya sido
                    obtenida como resultado de haber trabajado para cualquiera de las empresas del Grupo DMI.
                </p>
                <p>
                    No está permitido al colaborador:
                </p>
                <ul>
                    <li>Sacar de las instalaciones de las empresas de Grupo DMI o enviar por medios
                electrónicos a personas ajenas al Grupo DMI, las listas de clientes o proveedores,
                archivos, materiales de consulta e informes, aplicaciones informáticas, sistemas de
                procesamiento de datos y base de datos. (A menos que se cuente con autorización por escrito,
                ni los originales, ni las copias podrán sacarse de la empresa.)</li>
                    <li>Llevar o dejar las computadoras portátiles u objetos que contengan información de la empresa
                en lugares públicos, donde se tenga el riesgo de ser robados.</li>
                    <li>Sacar de las instalaciones de las empresas de Grupo DMI objetos de su propiedad, salvo
                en casos de autorización expresa, esto incluye los muebles, equipos, accesorios, carpetas,
                libros, documentos, etc. (Aunque dichos colaboradores hayan dejado de trabajar para Grupo,
                no podrán sacar de la empresa ningún objeto o documento sin la autorización de la dirección
                general). </li>
                    <li>No se permite ingresar Lap Tops de propiedad del colaborador para uso de su trabajo en la
                empresa y mucho menos llevarla a casa, excepto si sale de viaje y la utilizara en su estancia.</li>
                    <li>Dañar las instalaciones, por lo que se debe evitar clavar, barrenar, construir o destruir muros,
                pisos, techos o cualquiera de las instalaciones de Grupo DMI, sin la autorización por
                escrito de la dirección general. </li>
                </ul>
                <p>
                    Se deben cumplir con las medidas de seguridad para proteger los activos de la empresa de cualquier
                    abuso o movimiento no autorizado, así como de cualquier delito o abuso de confianza.
                </p>
                <p>
                    Es responsabilidad de los colaboradores hacer uso responsable de las redes sociales procurando
                    tener especial cuidado en no poner en riesgo la marca y la reputación de Grupo DMI.
                </p>
                <p>
                    No está permitido divulgar en las redes sociales de uso personal del colaborador información
                    referente a aspectos o procesos de Grupo DMI. Por lo anterior no está permitido publicar fotografías
                    o información o comunicados de uso exclusivo interno en la empresa.
                </p>
                <p>
                    Cualquier material, equipo, mobiliario, etc., que no sea necesario en cualquier departamento de
                    Grupo DMI, deberá ser entregado al responsable del área de Mantenimiento para que este disponga
                    de esos bienes como mejor le convenga a la empresa.
                </p>
                <p class="text-uppercase pt-3"><u><strong>5. Controles internos.</strong></u></p>
                <p>
                    En Grupo DMI se cuenta con un sistema de controles internos y segregación de funciones que
                    permite que las transacciones se lleven conforme a las políticas y procedimientos establecidos. Todos
                    los colaboradores se deberán apegar estrictamente a estas normas.
                </p>
                <p>
                    El sistema de controles internos comprende regulaciones, políticas y procedimientos por escrito, así
                    como revisiones por auditorías internas y externas dentro de las empresas que conforman el grupo
                    Todas las transacciones se deben ejecutar bajo los lineamientos publicados en nuestras políticas y
                    procedimientos, los cuales han sido comunicados a todo el personal a través de la Intranet
                    Corporativa.
                </p>
                <p>
                    Está prohibido realizar registros falsos o ficticios en cualquier sistema electrónico de las empresas
                    del Grupo.
                </p>
                <p>
                    Por ningún motivo se deben mantener fondos u otros activos en forma secreta o sin registrar en el
                    sistema.
                </p>
                <p>
                    Cualquier modificación a un contrato institucional o la elaboración de cualesquier instrumento que
                    represente una responsabilidad sensible para el Grupo, el Director Jurídico Corporativo deberá dar
                    su aprobación. Para los contratos laborales se deberá contar además con la autorización del Gerente
                    de Recursos Humanos Corporativo y el Director de Contraloría Corporativo.
                </p>
                <p>
                    Las personas que pongan en riesgo a cualquier empresa del grupo por una acción u omisión de este
                    tipo y que a consideración de la investigación de la autoridad competente tengan responsabilidad,
                    serán sujetas a los procesos legales que correspondan.
                </p>
                <p class="text-uppercase pt-3"><u><strong>6. Relación laboral.</strong></u></p>
                <p>
                    LLas virtudes, valores y la moral, son la base y el cimiento para llevar una vida ordenada dentro y fuera
                    del trabajo, por tal motivo todos los colaboradores de Grupo DMI deberán seguir normas de conducta
                    apegadas a dichas virtudes, valores y buenas costumbres de convivencia en su vida diaria.<br><br>
                    En Grupo DMI existen las mismas oportunidades para hombres y mujeres, y deberán tener las
                    mismas condiciones salariales para puestos iguales.
                </p>
                <p>
                    Atendiendo a las normas morales y de buena conducta no está permitido a los colaboradores:
                </p>
                <ul>
                    <li>Tener relaciones íntimas, con personal que labore con alguno de nuestros clientes o
                        proveedores. </li>
                    <li>Mantener una relación extramatrimonial entre dos colaboradores de Grupo DMI. (Cuando uno
                        o los 2 involucrados está en estado civil casado).</li>
                    <li>Cometer cualquier acto de acoso u hostigamiento sexual</li>
                    <li>Comete cualquier clase de faltas al respeto ó moral de la persona.
                (Toda denuncia se investigará por parte del comité de equidad de género de acuerdo a
                sus procesos vigentes).</li>
                    <li>Cometer cualquier clase de faltas al respeto o a la moral de la persona. </li>
                    <li>Usar o vender drogas dentro las instalaciones de Grupo DMI. </li>
                    <li>Ejercer cualquier tipo de violencia física o psicológica que dañe la integridad o de autoestima
                        de la otra persona.</li>
                    <li>Incumplir o infringir el Reglamento Interno de Trabajo, y la empresa lo considera grave.</li>
                </ul>
                <p class="text-uppercase pt-3"><u><strong>7. Relación con clientes y proveedores.</strong></u></p>
                <p>
                    Grupo DMI busca el liderazgo en el mercado a través de la mejora continua, la implementación de
                    las mejores prácticas y la honestidad, bajo ninguna circunstancia se permite el uso de mentiras,
                    trampas, dadivas u otros incentivos para lograr esa meta. (El fin no justifica los medios).
                </p>
                <p>
                    Se deben ejercitar el buen juicio y la moderación para evitar malos entendidos, por lo que está
                    terminantemente prohibido el ofrecer, dar, solicitar o recibir sobornos o cometer actos de corrupción.
                </p>
                <p>

                    Con el fin de crear y mantener la credibilidad y confianza entre Grupo DMI y sus clientes o
                    proveedores tenemos como principio el establecer como compromiso solo aquello con lo que
                    podamos cumplir cabalmente. Ahora bien, si por circunstancias imprevistas no resulta posible
                    concretar el compromiso contraído, es obligación de la persona involucrada informar a su jefe
                    inmediato y al cliente o proveedor las causas del incumplimiento del compromiso.
                </p>
                <p>
                    Como una muestra de respeto mutuo, Grupo DMI prohíbe a sus colaboradores hacer ofertas de
                    trabajo o contratar a empleados que laboren con sus proveedores, y pide en reciprocidad, el mismo
                    respeto a sus distribuidores y proveedores.
                </p>
                <p>
                    Grupo DMI no establece diferencias discriminatorias de ningún tipo, por lo que pide a sus
                    colaboradores que seleccionen a sus distribuidores, clientes y mercados, solo con base en criterios
                    de negocio.
                </p>
                <p>
                    Grupo DMI, en un marco de competitividad y respeto, busca otorgar a sus proveedores las mismas
                    oportunidades para contratarlos.
                </p>
                <p>
                    Grupo DMI se reserva el derecho de vetar al proveedor, distribuidor o acreedor que haya incurrido en
                    prácticas desleales para conseguir vender, comprar o dar servicio al Grupo.
                </p>
                <p>
                    Grupo DMI se reserva el derecho de vetar al proveedor, distribuidor o acreedor que infiltre personal
                    de la competencia en cualquiera de las instalaciones de Grupo DMI.
                </p>
                <p>
                    Grupo DMI exige a sus proveedores que por alguna causa tienen que ingresar a las instalaciones de
                    la empresa, sumo respeto y cuidado a las personas y a las instalaciones, pudiendo llegar a vetar al
                    proveedor que cometa una falta al respecto.
                </p>
                <p>
                    Los regalos, atenciones u otras cortesías para los proveedores o clientes actuales o potenciales de
                    Grupo DMI deben justificarse con propósitos comerciales legítimos.
                </p>
                <p>
                    Los colaboradores de Grupo DMI, no deben, por ningún motivo, recibir dinero como regalo, cualquier
                    omisión es motivo de rescisión de contrato de forma inmediata.
                </p>
                <p>
                    Los colaboradores de Grupo DMI, con autorización del Comité de Cumplimiento, pueden ofrecer
                    regalos a los clientes y proveedores, si lo que se les está dando:
                </p>
                    <ul>
                        <li>Es consistente con las prácticas usuales en los negocios.</li>
                        <li>No tiene un valor mayor a $ 500.00 pesos M.N. ni puede considerarse como soborno.
                        </li>
                        <li>No viola ninguna Ley aplicable ni a los estándares de ética profesional.</li>
                        <li>No avergonzará, ni a la empresa ni al empleado en caso de que llegue al dominio público. </li>
                        <li>Los gastos que ello suponga deben registrase en forma exacta y específica, de acuerdo a los
                            procedimientos establecidos.</li>
                    </ul>
                    <p>
                        <strong>
                            *Nota.- Si tienes alguna duda en la interpretación de los presentes conceptos, acércate con tu
                            Coordinador de Recursos Humanos o consulta con el Oficial de Cumplimiento.
                        </strong>
                    </p>
                    <p>
                        Por ningún motivo se pedirá a clientes o proveedores dinero en efectivo o que se deposite dinero en
                        cuentas personales, cualquier omisión será motivo de rescisión de contrato y en caso de que lo
                        amerite, se ejercitarán las acciones legales correspondientes.
                    </p>
                    <p>
                        Cualquier regalo, ofrecido por un tercero, a cualquier colaborador de Grupo DMI, deberá ser
                        entregado al departamento de Recursos Humanos, y quedará sujeto a la revisión y aprobación del
                        Comité de Cumplimiento, quien decidirá el destino del mismo.
                    </p>
                    <p>
                        Toda cortesía recibida para eventos, servicios o productos por las relaciones comerciales existentes
                        deberá ser entregada al departamento de Recursos Humanos para su rifa entre los colaboradores
                        del Grupo, con previa autorización del Comité de Cumplimiento.

                    </p>
                <p class="text-uppercase pt-3"><u><strong>8. Información Confidencial.</strong></u></p>
                <p>
                    La siguiente información se considera en forma enunciativa más no limitativa, información
                    confidencial o privilegiada:
                </p>
                <ul>
                    <li>Contable y proyecciones financieras</li>
                    <li>Planeaciones estratégicas o financieras</li>
                    <li>Planes de fusiones, adquisiciones, asociaciones, expansiones y de negocios</li>
                    <li>Operaciones con valores y financiamientos</li>
                    <li>Controversias judiciales o administrativas</li>
                    <li>Cambios organizacionales</li>
                    <li>Información personal de colaboradores del Grupo DMI incluyendo sueldos</li>
                    <li>Información patrimonial de la empresa y de los socios</li>
                    <li>Investigación y desarrollo de nuevos productos</li>
                    <li>Propiedad intelectual e industrial tal como secretos industriales, marcas registradas,
                        patentes y derechos de autor</li>
                    <li>Políticas y procedimientos, así como prácticas comerciales, operativas y administrativas</li>
                    <li>Listados de clientes y proveedores</li>
                    <li>Estructuras y políticas de precios</li>
                    <li>Información contenida en los masters de precios</li>
                    <li>Información del área de Sistemas, claves, contraseñas, passwords, que puedan poner
                    en riesgo la operación y funcionalidad de la empresa</li>
                </ul>
                <p>
                    Toda persona que ingrese a colaborar a Grupo DMI debe firmar una carta de confidencialidad en la
                    que se responsabilice de hacer buen uso de la información, en caso de no firmarla, no podrá ser
                    contratado por el Grupo.
                </p>
                <p>
                    Los colaboradores de Grupo DMI no tienen permitido revelar o comunicar información confidencial a
                    terceros, excepto cuando sea requerida por razones de negocio y se cuente con la autorización por
                    escrito de un miembro del Comité de Cumplimiento.
                </p>
                <p class="text-uppercase pt-3"><u><strong>9. Contingencia Sanitaria.</strong></u></p>
                <p>
                    En Grupo DMI, existe el compromiso de proteger la confidencialidad de aquellos colaboradores que
                    contraigan enfermedades contagiosas, asumiendo que no serán objeto de ningún tipo de
                    discriminación y se respetarán sus derechos laborales.
                </p>
                <p>
                    Será responsabilidad de los colaboradores de informar a Recursos Humanos en el caso de presentar
                    síntomas de cualquier enfermedad contagiosa que ponga en riesgo la salud de nuestros
                    colaboradores y clientes. En caso de contraer COVID-19, tener un familiar en casa o cuidar a alguien
                    con esta enfermedad deberá informar a Recursos Humanos.
                </p>
                <p>
                    En Grupo DMI, se ha conformado un Comité de Contingencias mismo que será responsable de emitir
                    los comunicados oficiales de la acciones en caso de contingencia sanitaria y medidas a seguir para
                    salvaguardar la salud de todos los colaboradores.
                </p>
                <p class="text-uppercase pt-3"><u><strong>10. Sistema de Denuncia.</strong></u></p>
                <p>
                    Es obligación de todos los colaboradores de reportar cualquier hecho o acto que implique una falta o
                    un incumplimiento al presente código de ética, un conflicto de interés suyo o de algún compañero
                    colaborador de Grupo DMI, cualquier omisión puede considerarse como una falta, para quien encubra
                    la situación.
                </p>
                <p>
                    La empresa cuenta con un Sistema de Denuncia a través de un sitio web como medio de
                    comunicación, para poder realizar cualquier tipo de denuncia. Todas las denuncias serán de carácter
                    anónimo y confidencial. El sitio web es <a href="https://eticadmi.com/">https://eticadmi.com/</a>
                </p>
                <p>
                    El sitio web está disponible las 24 horas del día, los 365 días del año y es completamente gratuito.
                </p>
                <p>
                    Todas las denuncias serán investigadas.
                </p>
                <p>
                    Grupo DMI, tiene el compromiso de proteger la confidencialidad de los denunciantes y asegura que
                    no existirán represalias en contra de aquellos denunciantes de buena fe.
                </p>
                <p>
                    Cuando una persona incurra en una falta para la organización, que sea comprobada por el Comité
                    de Cumplimiento, se dará una consecuencia que puede ir desde una amonestación hasta la rescisión
                    de contrato de acuerdo a las leyes laborales vigentes.
                </p>
                <p class="text-uppercase pt-3"><u><strong>11. GENERALES.</strong></u></p>
                <p>
                    El presente código de ética y sus reglas aplican para todo colaborador y/o familiar directo, como
                    condición de empleo.
                </p>
                <p>
                    Sin excepción alguna, todos los colaboradores de Grupo DMI deben cumplir los estándares de
                    conducta de la empresa, así como las normas, políticas y procedimientos de la misma.
