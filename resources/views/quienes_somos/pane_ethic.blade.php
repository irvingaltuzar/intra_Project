                    Recursos Humanos o con el Oficial de Cumplimiento.
                </p>
                <p>
                    Todos los colaboradores de Grupo DMI deberán firmar la carta compromiso de conflicto de interés,
                    el acuerdo de confidencialidad y le será entregado el presente código de ética, al iniciar la relación
                    laboral, aceptando conocer las políticas de Integridad y ética de Grupo DMI. Será responsabilidad del
                    departamento de Recursos Humanos, entregar estos formatos.
                </p>
                <p>
                    La falta de cumplimiento y observancia de este código de ética se considerará como una falta y será
                    motivo para iniciar medidas disciplinarias, que pueden ser, desde un plan de regularización, una
