                <h2>NUESTROS INICIOS</h2>
                <p class="text-center fw-bold">
                    Construir Molinos de Viento
                </p>
                <p class="text-justify">
                    El espíritu del viento anima nuestros proyectos. El <span class="fst-italic">Grupo DMI </span> (Desarrolladora Mexicana de Inmuebles) cumple tres décadas que han transformado el entorno de los lugares que intervenimos.  Vivir, trabajar, conocer, divertirse y cerrar negocios en un solo lugar, no sólo era un sueño. Ahora es una realidad para compartir en un ambiente moderno y sofisticado.
                    <br>
                    <br>
                    Nuestros proyectos y desarrollos han tomado en cuenta la necesidad de acortar distancias, optimizar el tiempo sin el desgaste de los traslados … en una palabra darnos tiempo para vivir … en armonía, convivir con los que amas y servir a los demás con distinción y profesionalismo.
                    <br>
                    <br>
                    El <span class="fst-italic">Grupo DMI </span>, ha hecho realidad una forma de vida que ha combinado la calidad y percepción de vivir en una comunidad mixta, que satisface todas las necesidades de una vida buena.  Dormir, despertar ante un paisaje, ejercitarse, desayunar, bajar a la oficina y cerrar negocios para luego celebrarlo en un mismo lugar … no tiene precio.
                    <br>
                    <br>
                    Para llegar a este concepto de vida, tuvimos que andar muchos caminos, hasta llegar a la sinopsis de un solo Andares.  Este concepto único en México se desarrolló con la suma de sueños y voluntades, largas horas de trabajo y la valiosa participación de nuestros inversores y colaboradores.
                    <br>
                    <br>
                    Treinta años han pasado desde que iniciamos el camino, bajo el liderazgo de un triunvirato de jóvenes empresarios imbuídos por el espíritu del viento. Ellos han sido las <span class="fst-italic">aspas del molino</span>, que al impulso y del genio empresarial del fundador, en la década de los noventa del siglo XX, sopló para desarrollar con talento y grandes profesionistas especializados, con una visión integral de los proyectos, desarrollos residenciales que apuestan por la innovación y el desarrollo de México en el marco del bien común.
                    <br>
                    <br>
                    Desde entonces hemos transformado más de veinte millones de metros cuadrados, en extraordinarios proyectos en armonía con la naturaleza para <span class="fst-italic">crear espacios que dan forma, y enaltece un estilo de vida.</span>
                    <br>
                    <br>
                    <span class="fst-italic">Los Robles, Virreyes, los Frailes y Tres Ríos</span>, son desarrollos residenciales con esta visión, que han transformado el horizonte del paisaje en México; así también, El Campanario un desarrollo residencial en Querétaro, con un espectacular Campo de Golf. Y que decir de El Molino, todo un desarrollo residencial integral ubicado en la mejor zona comercial, residencial y educativa en León, Guanajuato, rodeado de áreas verdes.
                    <br>
                    <br>
                    <span class="fst-italic">Punta Tiburón</span>, es un megaproyecto residencial enclavado en un contexto paradisiaco y un entorno natural en Veracruz, México, donde más de la mitad de sus casi cuatro millones de metros cuadrados, se han destinado a áreas verdes, marinas y canales de navegación.
                    <br>
                    <br>
                    Así es como hemos proyectado el futuro de las ciudades con visión, creando espacios vivos para la familia como centro de todo, con una infraestructura de vanguardia … Seguimos desarrollando proyectos como <span class="fst-italic">Acuarela en Guadalajara y Mayakoba Country Club</span> en Playa del Carmen.
                    <br>
                    <br>
                    Un sueño más hecho realidad; <span class="fst-italic">Andares</span>. Un Centro Comercial que marca un nuevo punto de partida en la ciudad y que incorpora vivienda, oficinas y lugares de esparcimiento.  Un modelo que ha tenido éxito sin precedentes en Guadalajara y que, como un ente vivo, no deja de innovar y expandirse en sus casi 14 hectáreas al poniente de la ciudad de Guadalajara.
                    <br>
                    <br>
                    Andares es, sin duda, el desarrollo insignia de DMI y alberga los cuatro Corporativos donde se proyectan y administran todos sus desarrollos, entre otros múltiples corporativos.
                    <br>
                    <br>
                    Sus colaboradores y empleados ya suman una cantidad considerable de talentos que, liderados por sus fundadores, dan sustento y confiabilidad a sus desarrollos que son un acontecimiento significativo en el rostro de la ciudad donde están presentes.
                    <br>
                    <br>
                    Buscar un trato justo y equitativo en la relación con la Empresa DMI, ha dado seguridad y estímulo, para colaborar con todas sus capacidades, al personal corporativo de esta empresa que, se inspirada en la Doctrina Social de la Iglesia Católica, se impregna del espíritu que ha soplado desde entonces.
                    <br>
                    <br>
                    Una cosa más … El éxito de este corporativo DMI era un secreto que reveló uno de los hermanos a un empresario en Guadalajara. Éste le preguntó:
                    <br>
                    <br>
                    –¿Cuál ha sido el éxito de sus empresas?
                    <br>
                    <br>
                    – Este éxito es un secreto que te voy a compartir y es que todos nuestros proyectos tienen un socio secreto y éste es nada más que “Cristo Rey”.
                    <br>
                    <br>
                    Y ahora ya tenemos el nombre para el viento, el que ha soplado en el seno de la familia y éste ha sido ¡toda una revelación!.

